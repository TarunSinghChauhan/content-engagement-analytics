-- ── 1. Drop and recreate tables (idempotent setup) ────────────────────────
DROP TABLE IF EXISTS play_events CASCADE;
DROP TABLE IF EXISTS content_catalogue CASCADE;

-- ── 2. Content Catalogue Table ────────────────────────────────────────────
CREATE TABLE content_catalogue (
    content_id          VARCHAR(20)  NOT NULL,
    title               VARCHAR(500),            -- Allow long titles
    genre               VARCHAR(100),
    content_duration    INTEGER,                 -- Minutes
    release_date        DATE,

    CONSTRAINT pk_content_catalogue PRIMARY KEY (content_id)
);

COMMENT ON TABLE content_catalogue IS
    'Reference dimension table: one row per unique piece of content. '
    'Maps content_id to human-readable title, genre, and duration metadata.';

-- ── 3. Play Events Table (500,000 rows) ───────────────────────────────────
CREATE TABLE play_events (
    event_id                  VARCHAR(15)  NOT NULL,
    user_id                   VARCHAR(12)  NOT NULL,
    content_id                VARCHAR(20)  NOT NULL,
    watch_date                DATE         NOT NULL,
    watch_duration_minutes    INTEGER      NOT NULL,
    content_duration_minutes  INTEGER      NOT NULL,
    rewatch_flag              SMALLINT     NOT NULL DEFAULT 0,
    shared_flag               SMALLINT     NOT NULL DEFAULT 0,
    device_type               VARCHAR(30),
    user_age_group            VARCHAR(10),
    user_region               VARCHAR(50),
    genre                     VARCHAR(100),

    CONSTRAINT pk_play_events        PRIMARY KEY (event_id),
    CONSTRAINT fk_content            FOREIGN KEY (content_id)
                                         REFERENCES content_catalogue(content_id),
    CONSTRAINT chk_rewatch_flag      CHECK (rewatch_flag IN (0, 1)),
    CONSTRAINT chk_shared_flag       CHECK (shared_flag  IN (0, 1)),
    CONSTRAINT chk_watch_duration    CHECK (watch_duration_minutes > 0),
    CONSTRAINT chk_content_duration  CHECK (content_duration_minutes > 0)
);

COMMENT ON TABLE play_events IS
    'Fact table: one row per streaming play event. '
    '500,000 rows covering 2,000 users × 500 titles × Jan 2023 – Dec 2024. '
    'Source: generated by data/generate_play_events.py using beta-distributed watch durations.';

COMMENT ON COLUMN play_events.rewatch_flag IS
    '1 if the user had previously watched this title; 0 otherwise. '
    'Overall rate: ~8% (higher for >85% completion events).';

COMMENT ON COLUMN play_events.shared_flag IS
    '1 if the user shared this content after watching; 0 otherwise. '
    'Overall rate: ~4–7% depending on genre.';

-- ── 4. Performance Indexes ────────────────────────────────────────────────
-- These indexes are essential for analytical query performance on 500K rows.

-- Query 1 & 5: Engagement score aggregation per content
CREATE INDEX idx_pe_content_id
    ON play_events (content_id);

-- Query 2: Decay curve (content × date combinations)
CREATE INDEX idx_pe_content_date
    ON play_events (content_id, watch_date);

-- Query 3: Audience segment cross-tab (genre × age × device)
CREATE INDEX idx_pe_segment
    ON play_events (genre, user_age_group, device_type);

-- Query 4: Cold start analysis (date filtering per content)
CREATE INDEX idx_pe_date
    ON play_events (watch_date);

-- General analytics: user-level queries
CREATE INDEX idx_pe_user_id
    ON play_events (user_id);

-- Partial index for rewatch events only (faster rewatch-rate queries)
CREATE INDEX idx_pe_rewatch
    ON play_events (content_id, watch_date)
    WHERE rewatch_flag = 1;

-- Partial index for share events only
CREATE INDEX idx_pe_shared
    ON play_events (content_id, watch_date)
    WHERE shared_flag = 1;

