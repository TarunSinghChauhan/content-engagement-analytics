-- ── 6. Post-Load Validation Queries ──────────────────────────────────────
-- Run these after loading to confirm data integrity.

-- 6a. Row counts
SELECT 'play_events'      AS table_name, COUNT(*) AS row_count FROM play_events
UNION ALL
SELECT 'content_catalogue',               COUNT(*)              FROM content_catalogue;
-- Expected: 500,000 | 500

-- 6b. Null check
SELECT
    SUM(CASE WHEN event_id                 IS NULL THEN 1 ELSE 0 END) AS null_event_id,
    SUM(CASE WHEN user_id                  IS NULL THEN 1 ELSE 0 END) AS null_user_id,
    SUM(CASE WHEN content_id              IS NULL THEN 1 ELSE 0 END) AS null_content_id,
    SUM(CASE WHEN watch_date              IS NULL THEN 1 ELSE 0 END) AS null_watch_date,
    SUM(CASE WHEN watch_duration_minutes  IS NULL THEN 1 ELSE 0 END) AS null_watch_dur,
    SUM(CASE WHEN content_duration_minutes IS NULL THEN 1 ELSE 0 END) AS null_content_dur
FROM play_events;
-- Expected: All zeros

-- 6c. Key distribution checks
SELECT
    COUNT(DISTINCT user_id)    AS unique_users,       -- Expected: ~2,000
    COUNT(DISTINCT content_id) AS unique_content,     -- Expected: 500
    MIN(watch_date)            AS earliest_event,     -- Expected: 2023-01-01
    MAX(watch_date)            AS latest_event,       -- Expected: 2024-12-31
    ROUND(AVG(rewatch_flag::NUMERIC) * 100, 2) AS rewatch_rate_pct,  -- Expected: ~8%
    ROUND(AVG(shared_flag::NUMERIC)  * 100, 2) AS share_rate_pct     -- Expected: ~4-5%
FROM play_events;

-- 6d. FK integrity check
SELECT COUNT(*) AS orphaned_events
FROM play_events pe
LEFT JOIN content_catalogue cc ON cc.content_id = pe.content_id
WHERE cc.content_id IS NULL;
-- Expected: 0

-- ── 7. Useful Analysis Helper View ───────────────────────────────────────
-- Pre-compute completion_pct to avoid repeating the LEAST(...) expression
-- in every analysis query.

CREATE OR REPLACE VIEW vw_play_events_enriched AS
SELECT
    pe.*,
    cc.title,
    cc.release_date,

    -- Completion fraction (capped at 1.0)
    LEAST(
        pe.watch_duration_minutes::NUMERIC / NULLIF(pe.content_duration_minutes, 0),
        1.0
    )                                               AS completion_fraction,

    -- Days since release (for decay analysis)
    (pe.watch_date - cc.release_date)               AS days_since_release,

    -- Watch date parts (for time-series analysis)
    DATE_TRUNC('week',  pe.watch_date)              AS week_start,
    DATE_TRUNC('month', pe.watch_date)              AS month_start,
    EXTRACT(YEAR  FROM pe.watch_date)::INTEGER      AS watch_year,
    EXTRACT(MONTH FROM pe.watch_date)::INTEGER      AS watch_month,
    EXTRACT(DOW   FROM pe.watch_date)::INTEGER      AS day_of_week   -- 0=Sun, 6=Sat

FROM play_events pe
LEFT JOIN content_catalogue cc ON cc.content_id = pe.content_id;

COMMENT ON VIEW vw_play_events_enriched IS
    'Enriched play events view joining content metadata and pre-computing '
    'completion fraction and date dimensions. Use this view in lieu of '
    'the raw play_events table for all analytical queries to reduce repetition.';

